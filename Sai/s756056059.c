#include <stdio.h>

int main() {
        int a[3][3];
        int n;
        int b[10];
        int i, j, k;

        for (i = 0; i < 3; i++) {
                for (j = 0; j < 3; j++) {
                        scanf(" %d", &a[i][j]);
                }
        }

        scanf(" %d", &n);

        for (k = 0; k < n; k++) {
                scanf(" %d", &b[k]);
        }

        for (i = 0; i < 3; i++) {
                for (j = 0; j < 3; j++) {
                        for (k = 0; k < n; k++) {
                                if (a[i][j] == b[k]) {
                                        a[i][j] = 0;
                                        break;
                                }
                        }
                }
        }

        if (((a[0][0] == 0) && (a[0][1] == 0) && (a[0][2] == 0)) ||
                        ((a[1][0] == 0) && (a[1][1] == 0) && (a[1][2])) ||
                        ((a[2][0] == 0) && (a[2][1] == 0) && (a[2][2])) ||
                        ((a[0][0] == 0) && (a[1][0] == 0) && (a[2][0])) ||
                        ((a[0][0] == 0) && (a[1][0] == 0) && (a[2][0])) ||
                        ((a[0][1] == 0) && (a[1][1] == 0) && (a[2][1])) ||
                        ((a[0][2] == 0) && (a[1][2] == 0) && (a[2][2])) ||
                        ((a[0][0] == 0) && (a[1][1] == 0) && (a[2][2])) ||
                        ((a[0][2] == 0) && (a[1][1] == 0) && (a[2][0])) )
        {
                printf("YES\n");
        } else {
                printf("NO\n");
        }

        return 0;
}
